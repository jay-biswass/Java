package BasicsOfJava;

public class Percentage {
    public static void main(String[] args) {
        double sci = 97.62;
        double mth = 84.91;
        double sst = 92.36;
        double hin = 74.22;
        double eng = 43.96;
        double sum = sci+mth+sst+hin+eng;
        double per = sum/5;
        System.out.println("The percentage is "+per+"%");

    }
}
